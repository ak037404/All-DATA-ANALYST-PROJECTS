-- 4. Business Questions + SQL Solutions
-- Q1. List all customers and their regions
SELECT 
    customer_name, region
FROM
    customers;

-- Q2. Show all products with their prices
SELECT 
    product_name, price
FROM
    products;
    
-- Q3. Total number of orders placed (COUNT(*))
SELECT 
    COUNT(*) AS total_orders
FROM
    orders;

-- Q4. Total quantity sold for each product (JOIN, GROUP BY)
SELECT 
    p.product_name, SUM(o.quantity) AS total_qty_sold
FROM
    orders o
        JOIN
    products p ON o.product_id = p.product_id
GROUP BY p.product_name;

-- Q5. Total revenue for each product (*)
SELECT 
    p.product_name, SUM(o.quantity * p.price) AS total_revenue
FROM
    orders o
        JOIN
    products p ON o.product_id = p.product_id
GROUP BY p.product_name; 

-- Q6. List all orders with customer name and order date 
SELECT 
    o.order_id, c.customer_name, o.order_date
FROM
    orders o
        JOIN
    customers c ON o.customer_id = c.customer_id;
    
-- Q7. Find orders placed in March 2024 (WHERE)
SELECT *
FROM orders
WHERE order_date BETWEEN '2024-03-01' AND '2024-03-31';

-- Q8. Total revenue generated by each region
SELECT c.region,
       SUM(o.quantity * p.price) AS region_revenue
FROM orders o
JOIN customers c ON o.customer_id = c.customer_id
JOIN products p ON o.product_id = p.product_id
GROUP BY c.region;

-- Q9. Find the most expensive product (ORDER BY, DESC, LIMIT)
SELECT product_name, price
FROM products
ORDER BY price DESC
LIMIT 1;

-- Q10. Count how many orders each customer has placed
SELECT c.customer_name,
       COUNT(o.order_id) AS total_orders
FROM customers c
LEFT JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_name;

